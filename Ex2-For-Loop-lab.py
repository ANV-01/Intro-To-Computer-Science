my_list = ['earth', 'exciting', 'day', 'laptop', 'electrochemical', 'blue', 'up']
largest_word = ''
largest_found = 0
for my_list2 in my_list:
    print(my_list2, end=" ")
    len_of_string = len(my_list2)
    print(len_of_string) #gets the length of my_list2 and prints it
    word_len = len(my_list2)
    if word_len > largest_found:
        largest_found = word_len
        largest_word = my_list2
print("Longest word is", largest_word) 
