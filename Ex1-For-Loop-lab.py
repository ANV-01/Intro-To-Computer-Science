for numbers in range(1, 16): #lists numbers 1-16
    print(numbers, end=" ")
print("\n----------------")
for numbers in range(10, 26): #lists numbers 10-26
    print(numbers, end=" ")
print("\n----------------")
for numbers in range(7, 30, +4): #lists numbers 7-30 by increments of 4
    print(numbers, end=" ")
print("\n----------------")
for numbers in range(23, 7, -3): #lists every 3rd number going down the list 23-7
    print(numbers, end=" ")
print("\n----------------")
for numbers in range(2, 21, +2): #lists numbers 2-21 by increments of 2
    print(numbers, end=" ")